// ราคาสินค้าทั้งหมด
let totalPrice = 27000;

// ส่วนลด 25% คำนวณเป็นจำนวนเงิน
let discountAmount = totalPrice * 0.25;

// ราคาที่ต้องจ่ายจริงหลังหักส่วนลด
let finalPrice = totalPrice - discountAmount;

console.log(`ราคาที่ต้องจ่ายจริง = ${finalPrice}`);